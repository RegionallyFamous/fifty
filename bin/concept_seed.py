"""Source-of-truth metadata for every concept on the bench.

This module is consulted by:

* ``bin/build-concept-meta.py``   — backfills ``mockups/<slug>.meta.json`` for
                                    every entry below, merging the
                                    auto-extracted ``palette_hex`` from
                                    ``bin/extract-palette.py``.
* ``bin/audit-concepts.py``       — clusters by palette_tags / type_genre /
                                    era / sector / hero_composition and
                                    writes ``docs/concepts/AUDIT.md``.
* ``bin/check-concept-similarity.py`` — runs the same clustering as a
                                    pre-commit gate (warnings only by
                                    default, allowlistable).

Why a Python dict rather than a YAML/CSV under ``mockups/`` directly:
    The 100-concept queue moves slowly and we want one human-readable
    file we can scroll, search, and grep. The generated per-concept
    ``mockups/<slug>.meta.json`` files are the *runtime* source consumed
    by ``bin/build-redirects.py`` (so the static gallery has zero Python
    deps at render time), but they are derived from this file. Edit
    here, run ``python3 bin/build-concept-meta.py``, commit both.

Schema, per concept:

    slug              str   — lower-kebab, matches ``mockups/mockup-<slug>.png``
    name              str   — display title shown on cards / detail pages
    blurb             str   — one sentence; what the storefront sells + tone
    palette_tags      list  — 3-4 color-family tokens (cream, oxblood, navy…)
    type_genre        str   — one of the type_genres in CONTROLLED_VOCAB below
    era               str   — one era token from CONTROLLED_VOCAB
    sector            str   — one sector token (workwear, food, gallery…)
    hero_composition  str   — one composition token (type-led, photo-hero…)
    type_specimen     str   — "Display: Foo. Body: Bar." hint for the build agent
"""

