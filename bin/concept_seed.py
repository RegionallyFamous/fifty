CONCEPTS_BY_SLUG: dict[str, dict] = {c["slug"]: c for c in CONCEPTS}


def all_slugs() -> list[str]:
    """Sorted list of every concept slug declared above."""
    return sorted(CONCEPTS_BY_SLUG.keys())


def validate() -> list[str]:
    """Return a list of validation errors (empty if everything passes).

    Used by ``bin/build-concept-meta.py`` and the test suite to make sure
    every entry has all required fields, every token is in the controlled
    vocabulary, and no two concepts share a slug.
    """
    errors: list[str] = []
    seen_slugs: set[str] = set()
    required = {
        "slug",
        "name",
        "blurb",
        "palette_tags",
        "type_genre",
        "era",
        "sector",
        "hero_composition",
        "type_specimen",
    }
    for c in CONCEPTS:
        slug = c.get("slug", "<missing>")
        missing = required - set(c.keys())
        if missing:
            errors.append(f"{slug}: missing fields {sorted(missing)}")
            continue
        if slug in seen_slugs:
            errors.append(f"{slug}: duplicate slug")
        seen_slugs.add(slug)
        for tag in c["palette_tags"]:
            if tag not in PALETTE_TOKENS:
                errors.append(f"{slug}: unknown palette token '{tag}'")
        if c["type_genre"] not in TYPE_GENRES:
            errors.append(f"{slug}: unknown type_genre '{c['type_genre']}'")
        if c["era"] not in ERAS:
            errors.append(f"{slug}: unknown era '{c['era']}'")
        if c["sector"] not in SECTORS:
            errors.append(f"{slug}: unknown sector '{c['sector']}'")
        if c["hero_composition"] not in HERO_COMPOSITIONS:
            errors.append(f"{slug}: unknown hero_composition '{c['hero_composition']}'")
    return errors
